const MIN_PASSPHRASE_LENGTH = 12;
const DEFAULT_AUTO_PASSPHRASE_LENGTH = 24;
const PASS_CHARSET =
  "ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz23456789!@#$%^&*()-_=+";
const FALLBACK_AUTOGENERATED_PASSPHRASE = "Vault-Local-Secret-2026!";
const HAS_UPPERCASE_REGEX = /[A-Z]/;
const HAS_LOWERCASE_REGEX = /[a-z]/;
const HAS_DIGIT_REGEX = /\d/;
const HAS_SYMBOL_REGEX = /[^A-Za-z0-9]/;
const ENV_ASSIGNMENT_REGEX = /^([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/;
const INLINE_ENV_COMMENT_REGEX = /\s+#/;
const ENV_LINE_SPLIT_REGEX = /\r?\n/u;
const ENV_EXPORT_PREFIX_REGEX = /^export\s+/u;
let envVariableIdCounter = 0;

const createVariableId = (): string => {
  envVariableIdCounter += 1;
  return `env-var-${String(envVariableIdCounter)}`;
};

const decodeDoubleQuotedEnvValue = (value: string): string =>
  value.replace(/\\([nrt"\\])/g, (fullMatch, group: string) => {
    if (group === "n") {
      return "\n";
    }

    if (group === "r") {
      return "\r";
    }

    if (group === "t") {
      return "\t";
    }

    if (group === '"') {
      return '"';
    }

    if (group === "\\") {
      return "\\";
    }

    return fullMatch;
  });

const normalizeEnvValue = (rawValue: string): string => {
  const trimmed = rawValue.trim();

  if (trimmed.length >= 2) {
    const firstCharacter = trimmed.at(0);
    const lastCharacter = trimmed.at(-1);

    if (firstCharacter === '"' && lastCharacter === '"') {
      return decodeDoubleQuotedEnvValue(trimmed.slice(1, -1));
    }

    if (firstCharacter === "'" && lastCharacter === "'") {
      return trimmed.slice(1, -1);
    }
  }

  const inlineCommentIndex = trimmed.search(INLINE_ENV_COMMENT_REGEX);
  if (inlineCommentIndex === -1) {
    return trimmed;
  }

  return trimmed.slice(0, inlineCommentIndex).trimEnd();
};

const parseBulkEnvInput = (
  rawInput: string
): { invalidLineCount: number; variables: ParsedEnvVariable[] } => {
  const parsedVariables: ParsedEnvVariable[] = [];
  let invalidLineCount = 0;

  for (const rawLine of rawInput.split(ENV_LINE_SPLIT_REGEX)) {
    const trimmedLine = rawLine.trim();

    if (trimmedLine.length === 0 || trimmedLine.startsWith("#")) {
      continue;
    }

    const normalizedLine = trimmedLine.replace(ENV_EXPORT_PREFIX_REGEX, "");
    const match = normalizedLine.match(ENV_ASSIGNMENT_REGEX);

    if (!match) {
      invalidLineCount += 1;
      continue;
    }

    const key = match[1]?.trim();
    if (!key) {
      invalidLineCount += 1;
      continue;
    }

    parsedVariables.push({
      key,
      value: normalizeEnvValue(match[2] ?? ""),
    });
  }

  return {
    invalidLineCount,
    variables: parsedVariables,
  };
};

const mergeImportedVariables = (
  existingVariables: EnvVariable[],
  importedVariables: ParsedEnvVariable[]
): EnvVariable[] => {
  if (importedVariables.length === 0) {
    return existingVariables;
  }

  const mergedVariables = existingVariables.map((variable) => ({
    ...variable,
  }));
  const hasFilledValues = mergedVariables.some(
    (variable) =>
      variable.key.trim().length > 0 || variable.value.trim().length > 0
  );

  const keyToIndex = new Map<string, number>();
  for (const [index, variable] of mergedVariables.entries()) {
    const existingKey = variable.key.trim();
    if (existingKey.length > 0 && !keyToIndex.has(existingKey)) {
      keyToIndex.set(existingKey, index);
    }
  }

  let importStartIndex = 0;
  if (!hasFilledValues && mergedVariables.length === 1) {
    const firstImport = importedVariables.at(0);
    if (firstImport) {
      mergedVariables[0] = {
        ...mergedVariables[0],
        key: firstImport.key,
        value: firstImport.value,
      };
      keyToIndex.set(firstImport.key, 0);
      importStartIndex = 1;
    }
  }

  for (const importedVariable of importedVariables.slice(importStartIndex)) {
    const existingIndex = keyToIndex.get(importedVariable.key);

    if (existingIndex !== undefined) {
      mergedVariables[existingIndex] = {
        ...mergedVariables[existingIndex],
        key: importedVariable.key,
        value: importedVariable.value,
      };
      continue;
    }

    mergedVariables.push({
      id: createVariableId(),
      key: importedVariable.key,
      value: importedVariable.value,
    });
    keyToIndex.set(importedVariable.key, mergedVariables.length - 1);
  }

  return mergedVariables;
};

const hasRequiredCharacterClasses = (value: string): boolean =>
  HAS_UPPERCASE_REGEX.test(value) &&
  HAS_LOWERCASE_REGEX.test(value) &&
  HAS_DIGIT_REGEX.test(value) &&
  HAS_SYMBOL_REGEX.test(value);

const generateSecurePassphrase = (
  length: number = DEFAULT_AUTO_PASSPHRASE_LENGTH
): string => {
  const cryptoObject = globalThis.crypto;
  if (!cryptoObject?.getRandomValues) {
    return FALLBACK_AUTOGENERATED_PASSPHRASE;
  }

  for (let attempt = 0; attempt < 5; attempt += 1) {
    const randomValues = new Uint32Array(length);
    cryptoObject.getRandomValues(randomValues);
    const generated = Array.from(
      randomValues,
      (value) => PASS_CHARSET[value % PASS_CHARSET.length]
    ).join("");

    if (
      generated.length >= MIN_PASSPHRASE_LENGTH &&
      hasRequiredCharacterClasses(generated)
    ) {
      return generated;
    }
  }

  return FALLBACK_AUTOGENERATED_PASSPHRASE;
};

const getDuplicateVariableKeys = (variables: EnvVariable[]): string[] => {
  const seenKeys = new Set<string>();
  const duplicateKeys = new Set<string>();

  for (const variable of variables) {
    const key = variable.key.trim();
    if (key.length === 0) {
      continue;
    }

    if (seenKeys.has(key)) {
      duplicateKeys.add(key);
      continue;
    }

    seenKeys.add(key);
  }

  return Array.from(duplicateKeys);
};

const buildEnvironmentMap = (
  variables: EnvVariable[]
): Record<string, string> => {
  const envMap: Record<string, string> = {};

  for (const variable of variables) {
    const key = variable.key.trim();
    const value = variable.value.trim();

    if (key.length > 0 && value.length > 0) {
      envMap[key] = value;
    }
  }

  return envMap;
};

const getStrengthToneClass = (label: string): string => {
  if (label === "Strong") {
    return "text-primary";
  }
  if (label === "Good") {
    return "text-yellow-500";
  }
  if (label === "Weak") {
    return "text-orange-500";
  }
  return "text-muted-foreground";
};

const getPassphraseStrength = (
  passphrase: string
): { color: string; label: string; percent: number } => {
  const length = passphrase.length;
  if (length === 0) {
    return { label: "", percent: 0, color: "bg-muted" };
  }
  if (length < MIN_PASSPHRASE_LENGTH) {
    return { label: "Too short", percent: 20, color: "bg-destructive" };
  }

  let score = 0;
  if (length >= 16) {
    score += 1;
  }
  if (length >= 24) {
    score += 1;
  }
  if (
    HAS_UPPERCASE_REGEX.test(passphrase) &&
    HAS_LOWERCASE_REGEX.test(passphrase)
  ) {
    score += 1;
  }
  if (HAS_DIGIT_REGEX.test(passphrase)) {
    score += 1;
  }
  if (HAS_SYMBOL_REGEX.test(passphrase)) {
    score += 1;
  }

  if (score <= 1) {
    return { label: "Weak", percent: 40, color: "bg-orange-500" };
  }
  if (score <= 3) {
    return { label: "Good", percent: 70, color: "bg-yellow-500" };
  }
  return { label: "Strong", percent: 100, color: "bg-primary" };
};

const getImportSourceLabel = (source: ImportSource): string => {
  if (source === "file") {
    return ".env file";
  }

  if (source === "clipboard") {
    return "clipboard";
  }

  return "pasted text";
};

const getEncryptValidationMessage = (
  duplicateKeys: string[],
  canEncrypt: boolean
): string | null => {
  if (duplicateKeys.length > 0) {
    return `Duplicate key${duplicateKeys.length > 1 ? "s" : ""} found: ${duplicateKeys.slice(0, 3).join(", ")}`;
  }

  if (!canEncrypt) {
    return "Add at least one complete variable and use a stronger passphrase.";
  }

  return null;
};

