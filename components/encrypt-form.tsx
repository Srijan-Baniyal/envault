const useBulkEnvImport = ({
  setValidationMessage,
  setVariables,
}: {
  setValidationMessage: Dispatch<SetStateAction<string | null>>;
  setVariables: Dispatch<SetStateAction<EnvVariable[]>>;
}) => {
  const [bulkImportInput, setBulkImportInput] = useState("");
  const [importMessage, setImportMessage] = useState<string | null>(null);
  const [uploadedFileName, setUploadedFileName] = useState<string | null>(null);

  const applyImportedVariables = useCallback(
    (rawInput: string, source: ImportSource, sourceFileName?: string) => {
      const { invalidLineCount, variables: parsedVariables } =
        parseBulkEnvInput(rawInput);

      if (parsedVariables.length === 0) {
        setImportMessage(null);
        setValidationMessage(
          "No valid KEY=VALUE lines detected. Paste .env or Vercel-style variables."
        );
        return;
      }

      setVariables((prev) => mergeImportedVariables(prev, parsedVariables));
      setValidationMessage(null);

      if (source === "file") {
        setBulkImportInput(rawInput);
        setUploadedFileName(sourceFileName ?? "uploaded.env");
      } else {
        setBulkImportInput("");
        setUploadedFileName(null);
      }

      const sourceLabel = getImportSourceLabel(source);
      const skippedLineSuffix =
        invalidLineCount > 0
          ? ` (${String(invalidLineCount)} invalid line${invalidLineCount === 1 ? "" : "s"} skipped)`
          : "";

      setImportMessage(
        `Imported ${String(parsedVariables.length)} variable${parsedVariables.length === 1 ? "" : "s"} from ${sourceLabel}${skippedLineSuffix}.`
      );
    },
    [setValidationMessage, setVariables]
  );

  const handleImportFromTextarea = useCallback(() => {
    if (bulkImportInput.trim().length === 0) {
      setValidationMessage("Paste one or more KEY=VALUE lines to import.");
      setImportMessage(null);
      return;
    }

    applyImportedVariables(bulkImportInput, "manual");
  }, [applyImportedVariables, bulkImportInput, setValidationMessage]);

  const handleBulkImportPaste = useCallback(
    (event: ClipboardEvent<HTMLTextAreaElement>) => {
      const pastedText = event.clipboardData.getData("text");

      if (pastedText.trim().length === 0) {
        return;
      }

      event.preventDefault();
      applyImportedVariables(pastedText, "clipboard");
    },
    [applyImportedVariables]
  );

  const importFromFile = useCallback(
    async (file: File) => {
      try {
        const fileContents = await file.text();
        applyImportedVariables(fileContents, "file", file.name);
      } catch {
        setImportMessage(null);
        setValidationMessage("Could not read the selected .env file.");
      }
    },
    [applyImportedVariables, setValidationMessage]
  );

  const handleEnvFileUpload = useCallback(
    async (event: ChangeEvent<HTMLInputElement>) => {
      const selectedFile = event.target.files?.[0];
      if (!selectedFile) {
        return;
      }

      await importFromFile(selectedFile);
      event.target.value = "";
    },
    [importFromFile]
  );

  const handleDroppedEnvFile = useCallback(
    async (file: File) => {
      await importFromFile(file);
    },
    [importFromFile]
  );

  const resetBulkImportState = useCallback(() => {
    setBulkImportInput("");
    setImportMessage(null);
    setUploadedFileName(null);
  }, []);

  return {
    bulkImportInput,
    handleDroppedEnvFile,
    handleBulkImportPaste,
    handleEnvFileUpload,
    handleImportFromTextarea,
    importMessage,
    resetBulkImportState,
    setBulkImportInput,
    uploadedFileName,
  };
};

